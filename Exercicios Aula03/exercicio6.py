'''Desenvolva um programa que leia um número inteiro e apresente o número
informado, seguido do seu antecessor e do seu sucessor. Use variáveis para
desenvolver o programa.'''

valor = int(input("Digite um numero inteiro: "))

antecessor = valor - 1
sucessor = valor + 1

print("O antecessor do numero é:", antecessor)
print("O sucessor do numero é:", sucessor)
